create_zone() {
  local resp
  resp=$(post "/api/stores/$STORE_ID/zones" "$2")
  local id; id=$(extract "$resp" '.data.id // .id // empty')
  if [[ -n "$id" && "$id" != "null" ]]; then
    ok "Zone: $1 -> $id"
  else
    info "Zone $1: $(extract "$resp" '.message // .error // .')"
  fi
}

